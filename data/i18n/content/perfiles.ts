export function getProfilesByLocale(locale: Locale): VisitorProfile[] {
  const trans = translations[locale]
  if (!trans || Object.keys(trans).length === 0) return profilesEs
  return profilesEs.map((p) => {
    const t = trans[p.slug]
    if (!t) return p
    return { ...p, ...t }
  })
}

export function getProfileBySlugLocale(locale: Locale, slug: string) {
  return getProfilesByLocale(locale).find((p) => p.slug === slug)
}
